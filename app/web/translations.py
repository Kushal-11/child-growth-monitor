def get_lang(request) -> str:
    """Return 'en' or 'mr' from cookie."""
    raw = request.cookies.get("lang", "en")
    return raw if raw in TRANSLATIONS else "en"


def get_strings(request) -> dict:
    """Translation dict for current request language."""
    lang = get_lang(request)
    return TRANSLATIONS.get(lang, TRANSLATIONS["en"])


def i18n_context(request) -> dict:
    """Merge into every TemplateResponse context."""
    lang = get_lang(request)
    return {
        "t": TRANSLATIONS.get(lang, TRANSLATIONS["en"]),
        "lang": lang,
    }
